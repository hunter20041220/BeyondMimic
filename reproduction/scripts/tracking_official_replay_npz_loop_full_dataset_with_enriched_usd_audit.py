#!/usr/bin/env python3
"""Run official replay_npz.py loop over all official-loop-converted motions.

This is the replay companion to
``tracking_official_csv_to_npz_loop_full_dataset_with_enriched_usd_audit.py``.
Each row executes the official ``whole_body_tracking/scripts/replay_npz.py``
loop body in a fresh process, with a local fake-WandB artifact pointing at one
of the NPZ files generated by the official csv_to_npz.py loop under the
resource-adjusted enriched-USD runtime patch.

The claim boundary is deliberately narrow: this proves full public-motion
coverage for the official replay loop body under the enriched USD patch. It is
not unpatched official URDF-converter replay, not trained-policy tracking
evaluation, not PPO, not DAgger, and not paper-level Fig. 5/Fig. 6 evidence.
"""

from __future__ import annotations

import csv
import json
import os
import subprocess
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


ROOT = Path("/mnt/infini-data/test/BeyondMimic")
SCRIPT = ROOT / "reproduction/scripts/tracking_official_replay_npz_loop_with_enriched_usd_audit.py"
CSV_FULL_AUDIT = (
    ROOT
    / "res/tracking/official_csv_to_npz_loop_full_dataset_with_enriched_usd/"
    "tracking_official_csv_to_npz_loop_full_dataset_with_enriched_usd_audit.json"
)
SINGLE_AUDIT = (
    ROOT
    / "res/tracking/official_replay_npz_loop_with_enriched_usd/"
    "tracking_official_replay_npz_loop_with_enriched_usd_audit.json"
)
OUT = ROOT / "res/tracking/official_replay_npz_loop_full_dataset_with_enriched_usd"
LOG_DIR = ROOT / "logs/tracking_official_replay_npz_loop_full_dataset_with_enriched_usd"
FAILED_DIR = ROOT / "res/failed_runs/tracking_official_replay_npz_loop_full_dataset_with_enriched_usd"
TARGET_GPU = int(os.environ.get("BM_OFFICIAL_REPLAY_FULL_DATASET_TARGET_GPU", "4"))
MAX_STEPS = int(os.environ.get("BM_OFFICIAL_REPLAY_FULL_DATASET_MAX_STEPS", "299"))
LIMIT = int(os.environ.get("BM_OFFICIAL_REPLAY_FULL_DATASET_LIMIT", "0"))


def load_json(path: Path) -> dict[str, Any]:
    if not path.is_file():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def write_table(rows: list[dict[str, Any]], path: Path, delimiter: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "motion",
        "status",
        "latest_blocker",
        "returncode",
        "duration_seconds",
        "official_loop_body_completed",
        "shutdown_warning",
        "official_loop_call_299",
        "fake_wandb_download",
        "g1_cfg_patched_to_enriched_usd",
        "motion_npz",
        "log",
        "wrapper_stdout_log",
    ]
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, delimiter=delimiter)
        writer.writeheader()
        for row in rows:
            writer.writerow({key: row.get(key, "") for key in fieldnames})


def selected_motion_rows() -> list[dict[str, Any]]:
    audit = load_json(CSV_FULL_AUDIT)
    rows = audit.get("rows") or []
    rows = [row for row in rows if row.get("status") == "ok" and row.get("output_npz")]
    rows = sorted(rows, key=lambda row: row["motion"])
    if LIMIT > 0:
        rows = rows[:LIMIT]
    return rows


def run_one(row: dict[str, Any]) -> dict[str, Any]:
    motion = row["motion"]
    motion_npz = Path(row["output_npz"])
    motion_out = OUT / "motions" / motion
    motion_out.mkdir(parents=True, exist_ok=True)
    fake_artifact_dir = ROOT / "tmp/tracking_official_replay_npz_loop_full_dataset_with_enriched_usd" / motion
    env = os.environ.copy()
    env.update(
        {
            "BM_OFFICIAL_REPLAY_LOOP_MOTION_NPZ": str(motion_npz),
            "BM_OFFICIAL_REPLAY_LOOP_FAKE_ARTIFACT_DIR": str(fake_artifact_dir),
            "BM_OFFICIAL_REPLAY_LOOP_REGISTRY_NAME": f"bm-local/{motion}_official_loop_motion:latest",
            "BM_OFFICIAL_REPLAY_LOOP_LOG_BASENAME": f"{motion}_official_replay_loop_enriched_usd",
            "BM_OFFICIAL_REPLAY_LOOP_TARGET_GPU": str(TARGET_GPU),
            "BM_OFFICIAL_REPLAY_LOOP_MAX_STEPS": str(MAX_STEPS),
            "PYTHONUNBUFFERED": "1",
        }
    )
    start = time.time()
    proc = subprocess.run(
        ["python3", str(SCRIPT)],
        cwd=ROOT,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        check=False,
    )
    stdout_log = LOG_DIR / f"{motion}_wrapper_stdout.log"
    stdout_log.parent.mkdir(parents=True, exist_ok=True)
    stdout_log.write_text(proc.stdout, encoding="utf-8")
    single = load_json(SINGLE_AUDIT)
    checks = single.get("checks") or {}
    markers = (single.get("run") or {}).get("markers") or {}
    interpretation = single.get("interpretation") or {}
    ok_statuses = {
        "ok_official_replay_loop_with_enriched_usd_patch",
        "ok_official_replay_loop_with_enriched_usd_patch_shutdown_warning",
    }
    ok = (
        proc.returncode == 0
        and single.get("status") in ok_statuses
        and bool(interpretation.get("official_loop_body_completed"))
        and bool(checks.get("fake_wandb_download_seen"))
        and bool(checks.get("g1_cfg_patched_to_enriched_usd"))
        and bool(checks.get("official_loop_call_299_seen"))
    )
    result = {
        "motion": motion,
        "status": "ok" if ok else "failed",
        "latest_blocker": single.get("latest_blocker", "missing_single_audit"),
        "returncode": proc.returncode,
        "duration_seconds": round(time.time() - start, 3),
        "official_loop_body_completed": bool(interpretation.get("official_loop_body_completed")),
        "shutdown_warning": bool(interpretation.get("shutdown_warning")),
        "official_loop_call_299": bool(markers.get("official_loop_call_299")),
        "fake_wandb_download": bool(markers.get("fake_wandb_download")),
        "g1_cfg_patched_to_enriched_usd": bool(markers.get("g1_cfg_patched_to_enriched_usd")),
        "motion_npz": str(motion_npz),
        "log": single.get("outputs", {}).get("log", ""),
        "wrapper_stdout_log": str(stdout_log),
        "single_audit": single,
    }
    motion_audit = motion_out / f"{motion}_official_replay_loop_enriched_usd_audit.json"
    motion_audit.write_text(json.dumps(result, indent=2, sort_keys=True), encoding="utf-8")
    result["motion_audit"] = str(motion_audit)
    if not ok:
        FAILED_DIR.mkdir(parents=True, exist_ok=True)
        failed_path = FAILED_DIR / f"{motion}_wrapper_stdout.log"
        failed_path.write_text(proc.stdout, encoding="utf-8")
        result["failed_log_copy"] = str(failed_path)
    return result


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    LOG_DIR.mkdir(parents=True, exist_ok=True)
    motion_rows = selected_motion_rows()
    rows = [run_one(row) for row in motion_rows]
    table_rows = [{key: value for key, value in row.items() if key != "single_audit"} for row in rows]
    ok_rows = [row for row in rows if row["status"] == "ok"]
    failed_rows = [row for row in rows if row["status"] != "ok"]
    checks = {
        "official_replay_loop_wrapper_exists": SCRIPT.is_file(),
        "csv_full_dataset_audit_exists": CSV_FULL_AUDIT.is_file(),
        "all_40_csv_loop_outputs_selected": len(motion_rows) == 40 if LIMIT == 0 else len(motion_rows) == LIMIT,
        "all_motion_npz_paths_exist": all(Path(row["motion_npz"]).is_file() for row in table_rows),
        "all_rows_ok": len(rows) > 0 and len(failed_rows) == 0,
        "all_rows_reached_official_loop_299": all(row["official_loop_call_299"] for row in rows),
        "all_rows_downloaded_fake_wandb_artifact": all(row["fake_wandb_download"] for row in rows),
        "all_rows_used_enriched_usd_patch": all(row["g1_cfg_patched_to_enriched_usd"] for row in rows),
        "uses_official_replay_npz_loop": True,
        "uses_official_csv_loop_npz_inputs": True,
        "uses_resource_adjusted_usd": True,
        "does_not_claim_unpatched_official_asset_complete": True,
        "does_not_claim_trained_policy_eval": True,
        "does_not_claim_paper_level_replay": True,
        "does_not_start_training": True,
    }
    aggregate = {
        "row_count": len(rows),
        "ok_count": len(ok_rows),
        "failed_count": len(failed_rows),
        "total_replayed_steps": sum(MAX_STEPS for row in ok_rows),
        "shutdown_warning_count": sum(1 for row in rows if row.get("shutdown_warning")),
        "total_duration_seconds": round(sum(float(row["duration_seconds"]) for row in rows), 3),
    }
    status = (
        "ok_official_replay_npz_loop_full_dataset_with_enriched_usd"
        if all(checks.values())
        else "ok_with_official_replay_npz_loop_full_dataset_blocker"
    )
    summary = {
        "status": status,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "experiment_type": "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd",
        "scope": (
            "Runs the official whole_body_tracking replay_npz.py loop over every NPZ produced by the full-dataset "
            "official csv_to_npz.py loop audit, while keeping the same resource-adjusted enriched-USD runtime patch. "
            "This is full public-motion coverage for the official replay loop body, but it is not unpatched official "
            "converter output, not trained-policy evaluation, not PPO, not DAgger, and not paper-level tracking."
        ),
        "config": {
            "target_gpu": TARGET_GPU,
            "max_steps_per_motion": MAX_STEPS,
            "limit": LIMIT,
            "csv_full_dataset_audit": str(CSV_FULL_AUDIT),
        },
        "aggregate": aggregate,
        "checks": checks,
        "rows": table_rows,
        "failed_rows": [row["motion"] for row in failed_rows],
        "outputs": {
            "json": str(OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_audit.json"),
            "rows_csv": str(OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_rows.csv"),
            "rows_tsv": str(OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_rows.tsv"),
            "motion_root": str(OUT / "motions"),
            "log_dir": str(LOG_DIR),
        },
        "interpretation": {
            "claim_level": "resource_adjusted_official_loop_full_public_motion_reference_replay",
            "goal_complete": False,
            "paper_level_tracking_eval_complete": False,
            "not_paper_level_reasons": [
                "uses resource-adjusted enriched USD instead of the unpatched official G1 URDF converter output",
                "uses official csv_to_npz loop NPZs generated under the same enriched-USD runtime patch",
                "does not evaluate a trained policy, success/fall rate, reward, or tracking error metrics",
                "does not train PPO and does not produce official paper rollout videos",
                "does not involve real robot hardware",
            ],
        },
    }
    write_table(table_rows, OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_rows.csv", ",")
    write_table(table_rows, OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_rows.tsv", "\t")
    (OUT / "tracking_official_replay_npz_loop_full_dataset_with_enriched_usd_audit.json").write_text(
        json.dumps(summary, indent=2, sort_keys=True), encoding="utf-8"
    )
    print(json.dumps({"status": status, "rows": len(rows), "json": summary["outputs"]["json"]}, sort_keys=True))


if __name__ == "__main__":
    main()
